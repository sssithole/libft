#include "libft.h"

int		ft_memcmp(const void *s1, const void *s2, size_t n)
{
	char *ps1;
	char *ps2;

	ps1 = (char *)s1;
	ps2 = (char *)s2;
	while (s1 == s2)
	{
		s1++;
		s2++;
		n--;
	}
	return (s1 - s2);
}
